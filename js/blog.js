function createPost() {
    var idParams = window.location.search.match(/[?&]id=([^&]+)/);
    const areaPrincipal = document.getElementById("area-principal");
    areaPrincipal.innerHTML = "";

    const findPost = blogsApi.find((blog) => {
        if (blog.id === Number(idParams[1])) {
            return true;
        }
    });

    const newElement = document.createElement("div");
    newElement.id = "postagem";
    newElement.innerHTML = `
    <span class="data-postagem">${findPost.data}</span></br>
                <h2>${findPost.titulo}</h2></br>
                <div>
                    <img class="img-postagem" src="${findPost.image}" />
                    <p>
                      ${findPost.texto.map((post) => {
                          return `
                        </br></br><strong>${post.titulo}</strong></br></br>
                        ${post.textoArea}
                        `;
                      })}
                    </p>
                </div>                
            </div>
    `;

    areaPrincipal.appendChild(newElement);
}

createPost();
